    FRUITS("Fruits"),

    GRAINS("Grains"),

    RED_MEAT("Red Meat"),

    SEAFOOD("Seafood"),

    POULTRY("Poultry"),

    FISH("Fish"),

    EGGS("Eggs"),

    NUTS_SEEDS("Nuts/Seeds");
}